class Ingredient {
  final String name;
  final String category;
  final String storageMethod;
  final int quantity;
  final DateTime expirationDate;
  final FileImage;

  Ingredient({
    required this.name,
    required this.category,
    required this.storageMethod,
    required this.quantity,
    required this.expirationDate,
    required this.FileImage,
  });
}